""" Functions for using calculator.pyc

Includes several operations: add, substract, multiply, divide, square, cube, power and mod.
"""



def add(num1, num2): 
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

def square(num1):
    return num1 * num1

def cube(num1):
    return num1**3

def power(num1, num2):
    return num1**num2

def mod(num1, num2):
    return  num1 % num2
